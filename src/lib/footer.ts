/**
 * All supported social media platforms in display order.
 */
const allPlatforms: SocialLink['platform'][] = [
  'facebook',
  'youtube',
  'x',
  'instagram',
  'threads',
  'wechat',
];

/**
 * Builds an array of SocialLink objects from the site configuration's social section.
 * Always returns all 6 platforms. If a URL is empty or undefined, the link has no url field.
 */
export function buildSocialLinks(social: SiteConfig['social']): SocialLink[] {
  return allPlatforms.map((platform) => {
    const url = social[platform];
    return {
      platform,
      ...(url ? { url } : {}),
      icon: platformIconPaths[platform],
    };
  });
}

/**
 * Determines how a social link should be rendered.
 * When a URL is provided (non-empty string), it should be rendered as a hyperlink.
 * When no URL is provided (undefined or empty string), it should render without a link.
 */
export function renderSocialLink(link: SocialLink): { hasLink: boolean; href?: string } {
  if (link.url && link.url.trim().length > 0) {
    return { hasLink: true, href: link.url };
  }
  return { hasLink: false };
}
